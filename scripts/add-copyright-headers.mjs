// Idempotent copyright/SPDX header insertion across the tracked tree.
// Usage:
//   node scripts/add-copyright-headers.mjs [--dry-run] [--verbose]  # insert
//   node scripts/add-copyright-headers.mjs --check                  # CI gate
//
// --check writes nothing and exits non-zero if any header-eligible source file
// is MISSING its SPDX header. Wire it into CI so the license coverage cannot
// silently decay as new files are added.

import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const REPO_ROOT = execSync('git rev-parse --show-toplevel').toString().trim();
const CHECK = process.argv.includes('--check');
const DRY_RUN = process.argv.includes('--dry-run') || CHECK;
const VERBOSE = process.argv.includes('--verbose');

const YEAR = 2026;
const HOLDER = 'Ailin One, Inc.';
const SPDX = 'AGPL-3.0-or-later';
const SOURCE_URL = 'https://github.com/ailinone/collective-intelligence';
const PRODUCT = 'Collective Intelligence Engine (ci)';

// REUSE-IgnoreStart — the lines below are a TEMPLATE the script emits into other
// files; they are not this file's own license declaration. Wrapping them keeps
// `reuse lint` from misreading the templated "SPDX-License-Identifier: ${SPDX}"
// as an (invalid) license expression for this script itself.
const BODY_LINES = [
  `Copyright (C) ${YEAR} ${HOLDER}`,
  '',
  `This file is part of ${PRODUCT}.`,
  'Licensed under the GNU Affero General Public License v3.0 or later.',
  'See LICENSE in the repository root, or <https://www.gnu.org/licenses/>.',
  '',
  `SPDX-License-Identifier: ${SPDX}`,
  `Source: ${SOURCE_URL}`,
];
// REUSE-IgnoreEnd

// path prefixes (relative to repo root, forward-slash) that must NEVER be touched.
const EXCLUDE_PREFIXES = [
  'api/src/generated/', // regenerated by `prisma generate` — header lives in the generator template instead
  'api/prisma/migrations/', // checksum-tracked by Prisma; editing an applied migration.sql breaks `migrate deploy`
];

// Optional private-only exclusion list: exact repo-relative paths that exist in
// the PRIVATE repo but are not part of the AGPL-licensed public product (deploy
// pipelines, secret-ops scripts, internal docs — the audit's EXCLUDE verdicts).
// The file is intentionally UNtracked, so it exists only here and never ships to
// the public repo. In the public repo the file is absent AND these paths are
// absent, so the header gate passes there without knowing their names.
const EXTERNAL_EXCLUDE_FILE = 'audit_workspace/oss-fork/header-exclude.txt';
let externalExcludes = new Set();
try {
  externalExcludes = new Set(
    readFileSync(path.join(REPO_ROOT, EXTERNAL_EXCLUDE_FILE), 'utf8')
      .split('\n')
      .map((s) => s.trim())
      .filter((s) => s && !s.startsWith('#'))
  );
} catch {
  /* absent (e.g. in the public repo) — no external exclusions */
}

const isExcluded = (p) => EXCLUDE_PREFIXES.some((prefix) => p.startsWith(prefix)) || externalExcludes.has(p);

// comment-style handlers. Each returns the header block as an array of lines
// (WITHOUT trailing blank separator — callers add one blank line after).
const styles = {
  slashSlash: (lines) => lines.map((l) => (l ? `// ${l}` : '//')),
  hash: (lines) => lines.map((l) => (l ? `# ${l}` : '#')),
  sqlDash: (lines) => lines.map((l) => (l ? `-- ${l}` : '--')),
  htmlComment: (lines) => ['<!--', ...lines, '-->'],
};

// extension -> style. Only extensions with an unambiguous, safe comment syntax.
const EXT_STYLE = {
  '.ts': 'slashSlash',
  '.js': 'slashSlash',
  '.mjs': 'slashSlash',
  '.cjs': 'slashSlash',
  '.prisma': 'slashSlash',
  '.py': 'hash',
  '.sh': 'hash',
  '.ps1': 'hash',
  '.yaml': 'hash',
  '.yml': 'hash',
  '.toml': 'hash',
  '.sql': 'sqlDash',
  '.md': 'htmlComment',
};

// filename (basename) overrides for files with no/ambiguous extension.
// Makefiles use '#' comments; a comment header at the very top is safe (the
// tab-sensitivity of recipe lines is unaffected by leading comment lines).
const NAME_STYLE = (basename) => {
  if (basename === 'Dockerfile' || basename.startsWith('Dockerfile.')) return 'hash';
  if (basename === 'Makefile' || basename === 'makefile' || basename.endsWith('.mk')) return 'hash';
  return null;
};

function styleFor(relPath) {
  const base = path.basename(relPath);
  const byName = NAME_STYLE(base);
  if (byName) return byName;
  const ext = path.extname(relPath);
  return EXT_STYLE[ext] || null;
}

function buildHeader(style) {
  const fn = styles[style];
  return fn(BODY_LINES).join('\n');
}

function alreadyHasHeader(content) {
  const head = content.split('\n', 12).join('\n');
  // Require BOTH the license identifier AND a copyright line — a bare
  // "SPDX-License-Identifier" with no copyright text is incomplete and fails
  // REUSE's coverage check even though this script's own (pre-fix) detection
  // considered it done. Files may carry copyright either as this script's own
  // "Copyright (C)" phrasing or as the REUSE-native "SPDX-FileCopyrightText"
  // tag (used by files with a different rights holder, e.g. CODE_OF_CONDUCT.md
  // under CC-BY-4.0 — never overwrite those with the generic AGPL header).
  const hasCopyright = head.includes('Copyright (C)') || head.includes('SPDX-FileCopyrightText');
  return head.includes('SPDX-License-Identifier') && hasCopyright;
}

function splitPreamble(content) {
  // Returns { preamble, rest } where preamble (BOM / shebang / docker syntax
  // directive / python coding declaration) must stay on the literal first
  // byte(s)/line(s) of the file. A leading BOM is peeled off before the
  // line-based checks below (it isn't a line of its own) and re-prepended
  // first — a BOM anywhere but byte 0 is not a BOM anymore, it's just a
  // stray U+FEFF character that trips ESLint's no-irregular-whitespace rule.
  let bom = '';
  if (content.charCodeAt(0) === 0xfeff) {
    bom = content[0];
    content = content.slice(1);
  }
  const lines = content.split('\n');
  let cut = 0;
  if (lines[0] && lines[0].startsWith('#!')) {
    cut = 1;
    if (lines[1] && /coding[:=]\s*[-\w.]+/.test(lines[1])) cut = 2;
  } else if (lines[0] && /^#\s*syntax\s*=/i.test(lines[0])) {
    cut = 1;
  }
  if (cut === 0) return { preamble: bom, rest: content };
  const preamble = bom + lines.slice(0, cut).join('\n') + '\n';
  const rest = lines.slice(cut).join('\n');
  return { preamble, rest };
}

function detectEol(content) {
  return content.includes('\r\n') ? '\r\n' : '\n';
}

const tracked = execSync('git ls-files', { cwd: REPO_ROOT, maxBuffer: 64 * 1024 * 1024 })
  .toString()
  .split('\n')
  .map((s) => s.trim())
  .filter(Boolean);

const stats = { written: 0, skippedHasHeader: 0, skippedNoStyle: 0, skippedExcluded: 0, errors: 0 };
const touched = [];

for (const rel of tracked) {
  if (isExcluded(rel)) {
    stats.skippedExcluded++;
    continue;
  }
  const style = styleFor(rel);
  if (!style) {
    stats.skippedNoStyle++;
    continue;
  }
  const abs = path.join(REPO_ROOT, rel);
  let content;
  try {
    content = readFileSync(abs, 'utf8');
  } catch (err) {
    stats.errors++;
    console.error(`ERROR reading ${rel}: ${err.message}`);
    continue;
  }
  if (alreadyHasHeader(content)) {
    stats.skippedHasHeader++;
    continue;
  }

  // IMPORTANT: preamble/rest already contain the file's ORIGINAL line endings
  // (whatever they are) and must not be touched — only the freshly generated
  // header text (always built with '\n') gets converted to match. A blanket
  // regex replace over the whole concatenated string would double up any '\r'
  // already present in preamble/rest (CRLF -> CRCRLF), corrupting the file.
  const eol = detectEol(content);
  const { preamble, rest } = splitPreamble(content);
  const header = buildHeader(style);
  const headerBlock = eol === '\r\n' ? (header + '\n\n').replace(/\n/g, '\r\n') : header + '\n\n';
  const normalized = preamble + headerBlock + rest;

  stats.written++;
  touched.push(rel);
  if (VERBOSE) console.log(`[${style}] ${rel}`);
  if (!DRY_RUN) writeFileSync(abs, normalized, 'utf8');
}

const mode = CHECK ? 'CHECK (CI gate, no files written)' : DRY_RUN ? 'DRY RUN (no files written)' : 'APPLIED';
console.log('\n--- add-copyright-headers summary ---');
console.log(`mode: ${mode}`);
console.log(`tracked files scanned: ${tracked.length}`);
console.log(`headers ${CHECK ? 'MISSING' : 'written'}: ${stats.written}`);
console.log(`skipped (already had SPDX header): ${stats.skippedHasHeader}`);
console.log(`skipped (no safe comment style / not code): ${stats.skippedNoStyle}`);
console.log(`skipped (excluded path — generated/migrations): ${stats.skippedExcluded}`);
console.log(`errors: ${stats.errors}`);

if (DRY_RUN) {
  const byExt = {};
  for (const rel of touched) {
    const base = path.basename(rel);
    const key = NAME_STYLE(base) ? 'Dockerfile*' : path.extname(rel) || '(no ext)';
    byExt[key] = (byExt[key] || 0) + 1;
  }
  console.log(`\n${CHECK ? 'files missing a header' : 'would-touch'} by type:`);
  for (const [k, n] of Object.entries(byExt).sort((a, b) => b[1] - a[1])) console.log(`  ${n}\t${k}`);
}

if (CHECK && stats.written > 0) {
  console.error(`\n✗ ${stats.written} source file(s) are missing the required SPDX/copyright header.`);
  console.error('  Run `node scripts/add-copyright-headers.mjs` to fix, then commit.');
  if (touched.length <= 40) touched.forEach((t) => console.error(`    ${t}`));
  process.exit(1);
}
if (CHECK) console.log('\n✓ All header-eligible source files carry an SPDX header.');
