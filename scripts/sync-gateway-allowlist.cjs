/**
 * Canonical generator — emits the nginx public-paths allowlist consumed by
 * the gateway. The super-monorepo `gateway/` directory is the SOLE canonical
 * home for the runtime artifact (any historical sibling repos are deleted).
 *
 * Pipeline (single-source):
 *   ci/openapi-spec.json
 *     → this generator
 *     → gateway/nginx/conf.d/hosts/generated/ci-api-public-paths.map.conf
 *     → consumed by gateway/nginx/conf.d/hosts/production.conf (`include` directive)
 *
 * Local fallback: when run outside the super-monorepo (e.g. ci-only CI lane),
 * the generator emits `dist/ci-api-public-paths.map.conf` so contract checks
 * still have a target to validate against.
 *
 * Why a regex form over the simpler [^/]+:
 *   - URL-decoded "/" is forbidden in single segments (matches OpenAPI semantics).
 *   - Encoded "%2F" is also forbidden so the regex is invariant under percent-encoding.
 *   - One special-case for /v1/models/{id} accepts an optional second segment to
 *     support provider/model notation (raw or %2F-encoded).
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = process.cwd();
const SPEC_PATH = path.resolve(ROOT, 'openapi-spec.json');
const GATEWAY_OUTPUT_PATH = path.resolve(
  ROOT,
  '..',
  'gateway',
  'nginx',
  'conf.d',
  'hosts',
  'generated',
  'ci-api-public-paths.map.conf'
);
const LOCAL_OUTPUT_PATH = path.resolve(ROOT, 'dist', 'ci-api-public-paths.map.conf');

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function isTemplateSegment(segment) {
  return segment.startsWith('{') && segment.endsWith('}') && segment.length > 2;
}

// Disallow decoded "/" and encoded "%2F" by default in single template segments.
// Keep regex nginx-map friendly by avoiding quantifiers with braces.
const TEMPLATE_SEGMENT_REGEX = '(?:[^/%]|%(?!2[fF])[0-9A-Fa-f][0-9A-Fa-f])+';

function toRegexPath(openApiPath) {
  if (openApiPath === '/v1/models/{id}') {
    // Allow canonical IDs, raw provider/model, and encoded provider%2Fmodel.
    // Enforces at most one separator to avoid opening multi-level paths.
    const modelIdPattern = `${TEMPLATE_SEGMENT_REGEX}(?:(?:/|%2[fF])${TEMPLATE_SEGMENT_REGEX})?`;
    return `^/v1/models/${modelIdPattern}$`;
  }
  if (openApiPath === '/') return '^/$';
  const segments = openApiPath.replace(/^\//, '').split('/');
  const normalized = segments.map((segment) => {
    if (isTemplateSegment(segment)) return TEMPLATE_SEGMENT_REGEX;
    return escapeRegex(segment);
  });
  return `^/${normalized.join('/')}$`;
}

function renderMapFile(exactPaths, regexPaths) {
  const lines = [];
  lines.push('# ==================================================================');
  lines.push('# GENERATED FILE - CI API public path allowlist');
  lines.push('# Source: ci/openapi-spec.json');
  lines.push('# Generated by: ci/scripts/sync-gateway-allowlist.cjs');
  lines.push('# Canonical runtime location: gateway/nginx/conf.d/hosts/generated/');
  lines.push('# Do not edit manually. Regenerate with `npm run sync:gateway:api-allowlist`.');
  lines.push('# ==================================================================');
  lines.push('');
  lines.push('map $ci_api_request_path $ci_api_public_allowed {');
  lines.push('    default 0;');
  lines.push('');
  lines.push('    # Exact public paths');
  for (const routePath of exactPaths) {
    lines.push(`    ${routePath} 1;`);
  }
  lines.push('');
  lines.push('    # Templated public paths');
  for (const rule of regexPaths) {
    lines.push(`    ~${rule.regex} 1; # ${rule.path}`);
  }
  lines.push('}');
  lines.push('');
  return `${lines.join('\n')}\n`;
}

function chooseOutputPath() {
  const gatewayRoot = path.resolve(ROOT, '..', 'gateway');
  return fs.existsSync(gatewayRoot) ? GATEWAY_OUTPUT_PATH : LOCAL_OUTPUT_PATH;
}

function main() {
  if (!fs.existsSync(SPEC_PATH)) {
    throw new Error(`OpenAPI spec not found: ${SPEC_PATH}`);
  }

  const spec = JSON.parse(fs.readFileSync(SPEC_PATH, 'utf8'));
  const pathEntries = Object.keys(spec.paths || {});
  if (pathEntries.length === 0) {
    throw new Error('No OpenAPI paths found. Aborting allowlist generation.');
  }

  const exactPaths = [];
  const templatedPaths = [];
  for (const routePath of pathEntries) {
    if (!routePath.startsWith('/')) continue;
    if (routePath.includes('{')) {
      templatedPaths.push({ path: routePath, regex: toRegexPath(routePath) });
    } else {
      exactPaths.push(routePath);
    }
  }

  exactPaths.sort((a, b) => a.localeCompare(b));
  templatedPaths.sort((a, b) => a.path.localeCompare(b.path));

  const outputPath = chooseOutputPath();
  const rendered = renderMapFile(exactPaths, templatedPaths);
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, rendered, 'utf8');

  console.log(
    JSON.stringify(
      {
        generated: true,
        specPath: path.relative(ROOT, SPEC_PATH),
        outputPath: path.relative(ROOT, outputPath),
        totalPaths: pathEntries.length,
        exactPaths: exactPaths.length,
        templatedPaths: templatedPaths.length,
      },
      null,
      2
    )
  );
}

main();
